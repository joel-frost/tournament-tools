
public class Team {
	
	private String teamName;
	private int elo;
	private int teamID;
	
	public String getTeamName()
	{
		return teamName;
	}
	
	public void setTeamName(String teamName)
	{
		this.teamName = teamName;
	}
	
	public int getElo()
	{
		return elo;
	}
	
	public void setElo(int elo)
	{
		this.elo = elo;
	}
	
	public int getTeamID()
	{
		return teamID;
	}
	
	public void setTeamID(int teamID)
	{
		this.teamID = teamID;
	}

}
